#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	last_c;

	last_c = ft_strlen(s);
	while (last_c >= 0)
	{
		if (s[last_c] == (char)c)
			return ((char *)s + last_c);
		last_c--;
	}
	return (NULL);
}
